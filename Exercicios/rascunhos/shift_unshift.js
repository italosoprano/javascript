//shift remove o ultimo item de um array. o unshift adiciona item ao fim do array

let a = [0, 1, 2, 3, 4, 5] //array com numeros

a.shift() //remove primeiro item de um array

console.log(a) //imprimiu sem o primeiro item

a.unshift(7, 8, 9) //adiciona itens nas primeiras posições

console.log(a) //imprimi com itens adicionados no inicio do array