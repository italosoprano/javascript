//pop remove o ultimo item de um array. o push adiciona item ao fim do array

let a = [0, 1, 2, 3, 4, 5] //array com numeros

a.pop() //remove ultimo item de um array

console.log(a) //imprimiu sem o ultimo item

a.push(7, 8, 9) //adiciona itens nas ultimas posições

console.log(a) //imprimi com itens adicionados ao fim do array